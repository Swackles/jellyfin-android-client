@Composable
internal fun AddUserCard(onClick: () -> Unit) {
    OutlinedCard(
        modifier = Modifier
            .padding(8.dp)
            .size(120.dp)
            .clickable { onClick() }
    ) {
        Column(
            modifier = Modifier.fillMaxSize(),
            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            Icon(
                Icons.Filled.Add,
                contentDescription = "Add user",
                modifier = Modifier
                    .width(48.dp)
                    .height(48.dp)
            )
        }
    }
}